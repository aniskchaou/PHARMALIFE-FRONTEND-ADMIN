

export default class Constructor {
    id: number
    manufacturer_name: string
    mobile: string
    phone: string
    fax: string
    city: string
    state: string
    country: string


    constructor(
        id: number,
        manufacturer_name: string,
        mobile: string,
        phone: string,
        fax: string,
        city: string,
        state: string,
        country: string
    ) {
        this.id = id
        this.manufacturer_name = manufacturer_name
        this.mobile = mobile
        this.phone = phone
        this.fax = fax
        this.city = city
        this.state = state
        this.country = country
    }





}